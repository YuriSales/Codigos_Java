package Codigos;

public class UtilizandoFor {

    public static void main(String[] args) {
        
        for (int a = 0; a <= 10; a++){
            System.out.println("Variável a: " + a);
        }
        
        System.out.println("------------------------------");
        
        for (int b = 10; b >= 0; b--){
            System.out.println("Variável b: " + b);
        }
        
        System.out.println("------------------------------");
        
    }
}
